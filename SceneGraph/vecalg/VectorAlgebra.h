/**
 * Helper class for vector algebra operations.
 */
#ifndef VECTORALGEBRA_H_
#define VECTORALGEBRA_H_

#include "Vector3d.h"

class VectorAlgebra
{
public:
	/**
	 * Multiplies a Vector3d object with a given factor and writes result into the given
	 * reference to a Vector3d object.
	 * @param vec 		the vector which will be multiplied scalar.
	 * @param faktor 	the scalar faktor for the multiplication.
	 * @param result	a refernce to a vector the result will be written to.
	 */
	static void multVec3d(Vector3d vec, double faktor, Vector3d& result);
};

#endif /*VECTORALGEBRA_H_*/
