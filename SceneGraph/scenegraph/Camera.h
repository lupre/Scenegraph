#ifndef CAMERA_H_
#define CAMERA_H_

#include "VCore.h"

namespace sg {
/**
 * Implementation of a camera core. Extends the class Core.
 */	
class Camera : public VCore
{
public:
	/** Simple constructor */
	Camera();
	/** Simple destructor */
	virtual ~Camera();
	/**
	 * Calls the method `visit` of the given Traverser object.
	 */
	virtual void accept(Traverser& t); 
};

}// namespace sg

#endif /*CAMERA_H_*/
